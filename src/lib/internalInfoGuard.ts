// #72 対策：AI内田さんが社内の固有情報（納期・会議日程・担当者など）を
// 推測で創作して断言する問題への「プロンプト外ガード」。
//
// 人格プロンプト（systemInstruction）の文言強化では抑えきれないことが
// 検証で分かっているため（Issue #72 参照）、社内情報を問う気配のある発言にだけ、
// 送信メッセージの直後（＝モデルが最も強く従う位置）に注意書きを機械的に添える。
// 注意書きはサーバー内でだけ付き、画面表示や会話の保存には含まれない。

// 社内の固有情報（予定・案件・人）を問う可能性が高い語。
// 誤ヒットしても、注意書きが「必要とする場合は」という条件付きなので害はない。
const INTERNAL_INFO_PATTERN =
  /納期|締め?切り?|〆切|期限|会議|ミーティング|打ち?合わせ|日程|予定|スケジュール|スケジュル|担当|案件|部署|議事録|誰/;

// 発言の直後に添える注意書き。
// 「分からないと言う」「推測の断言をしない」「確認するふりをしない」を明示する。
const GUARD_NOTE = `---
（システムからの注記：これは利用者の発言ではありません。このアプリはあなたに、社内の予定・案件・納期・会議・担当者・部署などの情報を一切渡していません。上の発言がそうした社内の固有情報を必要とする場合は、日付・人名・部署名を推測で作らず、最初のひと言で「分からない」と正直に伝えてください。「確か〜だったはず」のような推測の断言や、実際にはできない「確認しておくね」も言わないでください。分かる範囲の一般的な話や、本人に確認する・カレンダーを見るなどの次の行動の提案は歓迎です。）`;

// 社内情報を問う気配があるかどうか。
export function needsInternalInfoGuard(message: string): boolean {
  return INTERNAL_INFO_PATTERN.test(message);
}

// 必要なときだけ、発言の末尾に注意書きを付けたメッセージを返す。
// 不要なら元のメッセージをそのまま返す。
export function withInternalInfoGuard(message: string): string {
  return needsInternalInfoGuard(message) ? `${message}\n\n${GUARD_NOTE}` : message;
}
